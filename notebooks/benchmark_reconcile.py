# MAGIC %md
# MAGIC ### When to use MinTrace over bottom-up or top-down
# MAGIC
# MAGIC **MinTrace wins when:**
# MAGIC
# MAGIC - **You have information at multiple hierarchy levels that is not captured by any
# MAGIC   single level.** A portfolio model trained on 5 years of data may capture trend
# MAGIC   direction better than individual peril models with sparse data. A peril model
# MAGIC   captures seasonality better than the portfolio aggregate. MinTrace uses all of
# MAGIC   this simultaneously.
# MAGIC
# MAGIC - **The hierarchy is deep and the bottom-level perils are noisy.** For small
# MAGIC   low-frequency perils (subsidence, flood), individual period estimates are very
# MAGIC   noisy. Top-down information acts as a regulariser. Bottom-up propagates the
# MAGIC   noise upward. MinTrace finds the minimum-variance trade-off.
# MAGIC
# MAGIC - **You need to demonstrate coherence in a regulatory or reserve review.**
# MAGIC   FCA pricing reviews and Lloyd's capital model validations require that your
# MAGIC   modelled loss costs aggregate consistently. MinTrace guarantees coherence by
# MAGIC   construction with an auditable methodology (Wickramasuriya et al. 2019).
# MAGIC
# MAGIC - **You have reliable earned premium weights.** The premium-weighted variant used
# MAGIC   here gives high-EP perils more influence. If EP estimates are unreliable
# MAGIC   (e.g., a new product with limited exposure history), use OLS MinTrace instead.
# MAGIC
# MAGIC **Bottom-up is sufficient when:**
# MAGIC
# MAGIC - **You trust the peril models and the portfolio model has known issues.** For
# MAGIC   instance, during a period of rapid portfolio change (new product launch, large
# MAGIC   UW shift), the historical portfolio model will be less reliable than current
# MAGIC   cell models. Bottom-up is the conservative choice.
# MAGIC
# MAGIC - **The hierarchy is shallow (perils → portfolio, no intermediate covers).**
# MAGIC   With fewer nodes, the gain from full MinTrace is smaller and bottom-up
# MAGIC   aggregation is a simpler audit trail.
# MAGIC
# MAGIC **Limitations:**
# MAGIC
# MAGIC - MinTrace requires `hierarchicalforecast` (Nixtla). On Databricks serverless
# MAGIC   this adds ~30s install time but runs in milliseconds per call.
# MAGIC - PremiumWeightedMinTrace is a linear method. It does not handle structural
# MAGIC   breaks or regime changes in the hierarchy. For large structural shifts,
# MAGIC   reconcile each regime separately.
# MAGIC - Coherence is a property of the reconciled series. It does not guarantee
# MAGIC   accuracy relative to the true loss costs — only internal consistency.

