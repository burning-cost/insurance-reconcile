# MAGIC %md
# MAGIC ### Baseline 2: Top-Down
# MAGIC
# MAGIC Use the portfolio-level (top) forecast and disaggregate proportionally to
# MAGIC historical peril shares. This ignores the peril-level forecast entirely.
# MAGIC The result is coherent but loses all signal from individual peril models.

# COMMAND ----------

t0_td = time.perf_counter()

def top_down_disaggregate(
    lc_df: pd.DataFrame,
    ep_df: pd.DataFrame,
    train_lc: pd.DataFrame,
    train_ep: pd.DataFrame,
) -> pd.DataFrame:
    """Disaggregate portfolio forecast to perils proportionally to train-era shares."""
    result = lc_df.copy()

    # Historical peril shares of portfolio loss cost (exposure-weighted)
    peril_shares = {}
    total_portfolio_lc = train_lc["Home"].mean()
    for p in PERIL_COLS:
        # Share = (LC_peril * EP_peril) / (LC_portfolio * EP_total) = EP_share * LC_ratio
        ep_share = train_ep[p].mean() / train_ep["Home"].mean()
        lc_ratio = train_lc[p].mean() / max(train_lc["Home"].mean(), 1e-10)
        peril_shares[p] = float(ep_share * lc_ratio)

    # Normalise shares so they sum consistently with EP weighting
    share_total = sum(peril_shares.values())
    for p in PERIL_COLS:
        peril_shares[p] /= max(share_total, 1e-10)

    # Disaggregate: LC_peril = share * LC_home * (EP_home / EP_peril)
    ep_home = ep_df["Home"]
    for p in PERIL_COLS:
        ep_p = ep_df[p]
        result[p] = lc_df["Home"] * peril_shares[p] * ep_home / ep_p.clip(lower=1.0)

    # Re-aggregate covers from the disaggregated perils
    result = bottom_up_aggregate(result, ep_df)
    return result

