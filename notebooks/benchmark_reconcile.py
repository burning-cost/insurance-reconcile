def rmse_by_level(y_true: pd.DataFrame, y_pred: pd.DataFrame, cols: list) -> dict:
    """RMSE for each series column."""
    result = {}
    for col in cols:
        if col in y_true.columns and col in y_pred.columns:
            err = y_true[col].values - y_pred[col].values
            result[col] = float(np.sqrt(np.mean(err ** 2)))
    return result


def coherence_error(lc_df: pd.DataFrame, ep_df: pd.DataFrame) -> float:
    """Max percentage discrepancy across all hierarchy aggregation constraints."""
    max_disc = 0.0
    for cover, perils in COVER_PERILS.items():
        if cover not in lc_df.columns:
            continue
        ep_cover = sum(ep_df[p] for p in perils if p in ep_df.columns)
        lc_implied = sum(lc_df[p] * ep_df[p] for p in perils if p in lc_df.columns) / ep_cover.clip(lower=1.0)
        disc = float(((lc_df[cover] - lc_implied).abs() / lc_implied.clip(lower=1e-10)).max() * 100)
        max_disc = max(max_disc, disc)

    # Portfolio constraint
    all_covers = list(COVER_PERILS.keys())
    ep_port = sum(ep_df[c] for c in all_covers if c in ep_df.columns)
    lc_port_implied = sum(lc_df[c] * ep_df[c] for c in all_covers if c in lc_df.columns) / ep_port.clip(lower=1.0)
    if "Home" in lc_df.columns:
        disc_port = float(((lc_df["Home"] - lc_port_implied).abs() / lc_port_implied.clip(lower=1e-10)).max() * 100)
        max_disc = max(max_disc, disc_port)

    return max_disc


