# MAGIC %md
# MAGIC ### Baseline 1: Bottom-Up
# MAGIC
# MAGIC Use the bottom-level (peril) forecasts as-is and re-aggregate upward using
# MAGIC the correct premium-weighted formula. This ignores the aggregate-level forecast
# MAGIC entirely. The resulting forecasts are coherent by construction but may have
# MAGIC higher error at aggregate levels if the portfolio model contains useful information
# MAGIC that is not in the individual peril models.

# COMMAND ----------

t0_bu = time.perf_counter()

PERIL_COLS = ["Fire", "EoW", "Subsidence", "Flood", "Storm", "Theft", "AccidentalDamage"]
COVER_PERILS = {
    "Buildings": ["Fire", "EoW", "Subsidence", "Flood", "Storm"],
    "Contents": ["Theft", "AccidentalDamage"],
}

def bottom_up_aggregate(lc_df: pd.DataFrame, ep_df: pd.DataFrame) -> pd.DataFrame:
    """Re-aggregate peril forecasts to cover and portfolio using EP weights."""
    result = lc_df[PERIL_COLS].copy()

    # Cover level
    for cover, perils in COVER_PERILS.items():
        ep_cover = ep_df[perils].sum(axis=1)
        lc_cover = sum(lc_df[p] * ep_df[p] for p in perils) / ep_cover
        result[cover] = lc_cover

    # Portfolio level
    all_covers = list(COVER_PERILS.keys())
    ep_portfolio = sum(ep_df[c] for c in all_covers)
    lc_portfolio = sum(result[c] * ep_df[c] for c in all_covers) / ep_portfolio
    result["Home"] = lc_portfolio

    return result

