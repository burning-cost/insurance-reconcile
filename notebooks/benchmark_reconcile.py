# MAGIC %md
# MAGIC # Benchmark: insurance-reconcile (MinTrace) vs Bottom-Up Aggregation
# MAGIC
# MAGIC **Library:** `insurance-reconcile` — MinTrace hierarchical forecast reconciliation
# MAGIC with insurance semantics: earned premium weighting, loss ratio transforms, and
# MAGIC peril tree DSL.
# MAGIC
# MAGIC **Baseline 1 (Bottom-Up):** Take only the peril-level (bottom) forecasts as the
# MAGIC truth. Aggregate upward using the correct premium-weighted formula. This is what
# MAGIC many teams do in practice: trust the cell models, aggregate mechanically.
# MAGIC
# MAGIC **Baseline 2 (Top-Down):** Disaggregate the portfolio-level forecast proportionally
# MAGIC to historical peril shares. This is the other common approach: trust the aggregate
# MAGIC model, push it down.
# MAGIC
# MAGIC **Dataset:** Synthetic UK home insurance (7 perils, 2 covers, 1 portfolio) from
# MAGIC `simulate_incoherent_uk_home()` — 24 monthly periods with intentional discrepancies
# MAGIC injected into aggregate-level forecasts to simulate independently-run models.
# MAGIC
# MAGIC **Date:** 2026-03-13
# MAGIC
# MAGIC **Library version:** 0.1.0
# MAGIC
# MAGIC ---
# MAGIC
# MAGIC The core question is what reconciliation method gives the best forecast accuracy
# MAGIC when the base forecasts are incoherent — i.e., when peril models and portfolio
# MAGIC models give different answers, as they always do when run independently.
# MAGIC
# MAGIC The benchmark simulates a realistic situation: run an independent forecast at
# MAGIC each hierarchy level, measure the incoherence (how much do aggregates disagree
# MAGIC from the bottom-up implied value), then compare what happens to forecast accuracy
# MAGIC after each reconciliation method is applied.
# MAGIC
# MAGIC We evaluate on a held-out test set using RMSE at each hierarchy level and
# MAGIC a coherence error metric (max discrepancy percentage across hierarchy nodes).
# MAGIC
# MAGIC **Key metrics:** RMSE at peril/cover/portfolio level, coherence error before
# MAGIC and after reconciliation, MASE (scale-normalised accuracy).

